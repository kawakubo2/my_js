console.log('複合代入演算子(compound assignment operator')
let x = 10;
console.log(`x = ${x}`);
console.log('x += 5'); // x += 5 --> x = x + 5
x += 5;
console.log(`x = ${x}`);

console.log('x -= 7');
x -= 7; // x = x - 7;
console.log(`x = ${x}`);

console.log('x *= 2');
x *= 2; // x = x * 2;
console.log(`x = ${x}`);

console.log('x /= 5');
x /= 5; // x = x / 5;
console.log(`x = ${x}`);
