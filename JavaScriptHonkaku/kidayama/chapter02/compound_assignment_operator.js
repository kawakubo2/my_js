// compound_assignment_operator.js
console.log('--- 複合代入演算子 ---');
let x = 10;
console.log(`x = ${x}`);
x += 2; // x = x + 2
console.log(`x += 2 ---> ${x}`);
x -= 7; // x = x - 7;
console.log(`x -= 7 ---> ${x}`);
x *= 5; // x = x * 5;
console.log(`x *= 5 ---> ${x}`);
x /= 10;
console.log(`x /= 10 ---> ${x}`);